def clustered_w_random_xburst_random_circuit(
    num_qubits: int,
    *,
    block_size: int = 8,
    depth: int | Sequence[int] | None = None,
    seed: int | None = 1337,
) -> Circuit:
    """Prepare W clusters with random, cross-block burst and random stages."""

